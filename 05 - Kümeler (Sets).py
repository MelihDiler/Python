#* Set fonksiyonun içine parçalanabilecek veri yapısı yazarsan bir kümede birleştirir.
#*------------------------------------------------------------------------------------
python = set("PYTHON")
print(python)                                    #*     Çıktı: {'H', 'T', 'N', 'O', 'Y', 'P'}  -->  Karakterleri tek tek kümeye koydu
python2 = set([1,2,3,4,5])
print(python2)                                   #*     Çıktı: {1, 2, 3, 4, 5}  -->  İçine [] ile liste verdik bize kümeye çevirdi.
python3 = set((1,2,3,4,5))
print(python3)                                   #*     Çıktı: {1, 2, 3, 4, 5}  -->  İçine () ile demet verdik bize kümeye çevirdi.
