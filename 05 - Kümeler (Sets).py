kume1 = {"Sarı", "Mavi", "Yeşil", "Kırmızı", "Siyah"}
kume2 = {"Sarı", "Mavi", "Yeşil", "Beyaz", "Gri"}

print(kume1.intersection(kume2))                 #!     Çıktı: {'Yeşil', 'Mavi', 'Sarı'}

#!----------------------------------------------------------------------------------------------------------------------------------------
#*----------------------------------------------------------------------------------------------------------------------------------------
#*                                          BIRLESIMLERINI BULMA                                            
#*----------------------------------------------------------------------------------------------------------------------------------------

print(kume1.union(kume2))                        #*     Çıktı: {'Mavi', 'Sarı', 'Kırmızı', 'Siyah', 'Beyaz', 'Yeşil', 'Gri'}  -->  Kümelerde her üründen bir tane olduğu için Sarı, Mavi, Yeşil değerlerini bir kere verdi. Çünkü bize verdiği şey de bir küme.

#*----------------------------------------------------------------------------------------------------------------------------------------
#?----------------------------------------------------------------------------------------------------------------------------------------
#?                                   BIRINDE OLUP DIGERINDE OLMAYANLAR
#?----------------------------------------------------------------------------------------------------------------------------------------

print(kume1.difference(kume2))                   #?     Çıktı: {'Siyah', 'Kırmızı'}  -->  Kume 1'de olup kume 2'de olmayanların kumesi.
print(kume2.difference(kume1))                   #?     Çıktı: {'Beyaz', 'Gri'}      -->  Kume 2'de olup kume 1'de olmayanların kumesi.

